    <h1>Politica de Privacidade</h1>
    <p class="updated">Ultima atualizacao: 14 de maio de 2026</p>

    <p>A <strong>Flut Tecnologia</strong> ("Flut", "nos", "nosso") opera a plataforma <strong>CRM Flut</strong> (crm.flut.com.br). Esta politica descreve como coletamos, usamos, armazenamos e protegemos suas informacoes pessoais.</p>

    <h2>1. Informacoes que coletamos</h2>
    <ul>
        <li><strong>Dados de cadastro:</strong> nome, e-mail, telefone e empresa, fornecidos durante o registro ou onboarding.</li>
        <li><strong>Dados de uso:</strong> interacoes com a plataforma, logs de acesso, enderecos IP e tipo de navegador.</li>
        <li><strong>Mensagens do WhatsApp:</strong> conteudo de conversas processadas pela plataforma para fins de atendimento ao cliente, armazenadas de forma segura.</li>
        <li><strong>Dados de integracao:</strong> informacoes recebidas via APIs de terceiros (Meta WhatsApp Cloud API, Evolution API) conforme configurado por cada empresa.</li>
    </ul>

    <h2>2. Como usamos suas informacoes</h2>
    <ul>
        <li>Fornecer, operar e manter a plataforma CRM Flut.</li>
        <li>Processar e gerenciar atendimentos via WhatsApp.</li>
        <li>Enviar notificacoes relacionadas ao servico (push, e-mail).</li>
        <li>Melhorar a experiencia do usuario e a performance da plataforma.</li>
        <li>Cumprir obrigacoes legais e regulatorias.</li>
    </ul>

    <h2>3. Compartilhamento de dados</h2>
    <p>Nao vendemos, alugamos ou compartilhamos seus dados pessoais com terceiros para fins de marketing. Podemos compartilhar dados com:</p>
    <ul>
        <li><strong>Provedores de servico:</strong> Meta (WhatsApp Business API), provedores de hospedagem e infraestrutura, exclusivamente para operacao da plataforma.</li>
        <li><strong>Obrigacoes legais:</strong> quando exigido por lei, ordem judicial ou autoridade competente.</li>
    </ul>

    <h2>4. Armazenamento e seguranca</h2>
    <p>Seus dados sao armazenados em servidores seguros com criptografia em transito (TLS/SSL). Implementamos medidas tecnicas e organizacionais para proteger contra acesso nao autorizado, perda ou destruicao de dados.</p>

    <h2>5. Retencao de dados</h2>
    <p>Mantemos seus dados pelo tempo necessario para fornecer o servico ou conforme exigido por lei. Voce pode solicitar a exclusao dos seus dados a qualquer momento (veja secao 7).</p>

    <h2>6. Seus direitos (LGPD)</h2>
    <p>Conforme a Lei Geral de Protecao de Dados (LGPD), voce tem direito a:</p>
    <ul>
        <li>Acessar seus dados pessoais.</li>
        <li>Corrigir dados incompletos ou desatualizados.</li>
        <li>Solicitar a exclusao dos seus dados.</li>
        <li>Revogar consentimento a qualquer momento.</li>
        <li>Solicitar portabilidade dos dados.</li>
    </ul>

    <h2>7. Exclusao de dados</h2>
    <p>Para solicitar a exclusao dos seus dados, acesse <a href="/data-deletion">crm.flut.com.br/data-deletion</a> ou envie um e-mail para <strong>privacidade@flut.com.br</strong>.</p>

    <h2>8. Cookies</h2>
    <p>Utilizamos cookies essenciais para funcionamento da plataforma (sessao, autenticacao). Nao utilizamos cookies de rastreamento de terceiros.</p>

    <h2>9. Alteracoes nesta politica</h2>
    <p>Podemos atualizar esta politica periodicamente. Alteracoes significativas serao comunicadas por e-mail ou aviso na plataforma.</p>

    <h2>10. Contato</h2>
    <p>Para duvidas sobre privacidade, entre em contato:</p>
